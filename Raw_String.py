# Raw String adalah string atau teks yang ditampilkan apa adanya
#   Jadi jika anda menulis dengan melibatkan escape character , maka tidak ada yang terjadi
#   dan ditampilkan dalam bentuk teks biasa

# Raw string dinyatakan dengan huruf 'r' yang diketik sebelum tanda kutip pada teks

print ("Contoh penggunaan raw string")
print ("Perkenalkan nama saya Wiliam Santoso , saya pemilik coffee shop will\'s bar atau \"WB\" pekerjaan sebagai barista pengaduk kopi \n")

print ("Contoh tanpa penggunaan raw string")
print (r"Perkenalkan nama saya Wiliam Santoso , saya pemilik coffee shop will\'s bar atau \"WB\" \n \t pekerjaan sebagai barista\\pengaduk kopi")