x = 10
y = 20
print (x)
#akan menghasilkan angka 10
print(y)
#akan menghasilkan angka 20

x, y = y, x
print(x)
#akan menghasilkan angka 20
print(y)
#akan menghasilkan angka 10

#CATATAN
#   Swap variables adalah proses menukar nilai antara beberapa variabel
#   didalam contoh diatas ,Baris kode x, y = y, x ; digunakan untuk menukar nilai masing-masing variabel
#   dimana variabel (x) menyimpan nilai varibel (y) dan variabel (y) menyimpan variabel (x)